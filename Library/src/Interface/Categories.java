    public void resetStatus() {
        /*Volvemos a llenar la tabla y reiniciamos todos
        los campos y botones que se encentren*/
        showCategories();
        jTextField1.setText("");
        jButton1.setEnabled(false);
        jButton2.setEnabled(false);
        jButton3.setEnabled(false);
        jButton4.setEnabled(false);
        id = 0;
        Arrays.fill(fields, false);
    }
    
    public void checkFields() {
        /*Aqui ya se hace la validacion como tal
        estableciendo un contador que es proporcional
        al numero de campos*/
        int cnt = 0;
        for (int x = 0; x < fields.length; x++) {
            if (fields[x] == true) {
                cnt++;
            }
        }
        if (cnt == 1) {
            if (id == 0) {
                jButton1.setEnabled(true);
            }
        } else {
            jButton1.setEnabled(false);
        }
    }
    
