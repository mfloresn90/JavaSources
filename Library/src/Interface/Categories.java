    public void showCategories() {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("ID");
        dtm.addColumn("Categoría");
        dtm.addColumn("Número de libros");
        
        try {
            String[] data = new String[3];
            Statement set = cn.createStatement();
            ResultSet rst = set.executeQuery("SELECT * FROM categories");
            while (rst.next()) {
                data[0] = rst.getString(1);
                data[1] = rst.getString(2);
                data[2] = rst.getString(3);
                dtm.addRow(data);
            }
            categoryTable.setModel(dtm);
        } catch (Exception ex) {
            System.out.println("Mensaje: \n" + ex.getMessage());
        }
    }

