    private void ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarActionPerformed
        int row = DataTable.getSelectedRow();
        
        System.out.println("El valor de la fila es: " + row);
        
        if (row >= 0) {
            id = Integer.parseInt(DataTable.getValueAt(row, 0).toString());
            matricula.setText(DataTable.getValueAt(row, 1).toString());
            grupo.setText(DataTable.getValueAt(row, 2).toString());
            nombre.setText(DataTable.getValueAt(row, 3).toString());
            papellido.setText(DataTable.getValueAt(row, 4).toString());
            mapellido.setText(DataTable.getValueAt(row, 5).toString());
            dir.setText(DataTable.getValueAt(row, 6).toString());
            tel.setText(DataTable.getValueAt(row, 7).toString());
            email.setText(DataTable.getValueAt(row, 8).toString());
            nacimiento.setText(DataTable.getValueAt(row, 9).toString());
            String gene = DataTable.getValueAt(row, 10).toString();
            if (gene.endsWith("Femenino")) {
                genero.setSelectedIndex(1);
            } else if (gene.endsWith("Masculino")) {
                genero.setSelectedIndex(2);
            } else
                genero.setSelectedIndex(0);
            update.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "Sorry my friend!! :'( " + row);
        }
    }//GEN-LAST:event_ActualizarActionPerformed

