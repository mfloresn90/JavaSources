    public void showData() {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("ID");
        dtm.addColumn("Matricula");
        dtm.addColumn("Grupo");
        dtm.addColumn("Nombre");
        dtm.addColumn("Apellido P");
        dtm.addColumn("Apellido M");
        dtm.addColumn("Dirección");
        dtm.addColumn("Teléfono");
        dtm.addColumn("E-mail");
        dtm.addColumn("Nacimiento");
        dtm.addColumn("Sexo");
        dtm.addColumn("Registro");

        DataTable.setModel(dtm);

        try {
            String[] data = new String[12];
            Statement set = cn.createStatement();
            ResultSet rst = set.executeQuery("SELECT * FROM students");
            while (rst.next()) {
                data[0] = rst.getString(1);
                data[1] = rst.getString(2);
                data[2] = rst.getString(3);
                data[3] = rst.getString(4);
                data[4] = rst.getString(5);
                data[5] = rst.getString(6);
                data[6] = rst.getString(7);
                data[7] = rst.getString(8);
                data[8] = rst.getString(9);
                data[9] = rst.getString(10);
                data[10] = rst.getString(11);
                data[11] = rst.getString(12);
                dtm.addRow(data);
            }
            DataTable.setModel(dtm);
        } catch (Exception ex) {
            System.out.println("Mensaje: \n" + ex.getMessage());
        }
    }

