    public void mostrarDatoPlataforma() {
        boxPlataforma.removeAllItems();
        boxPlataforma.addItem("Selecciona plataforma: ");
        try {
            Statement stt = cn.createStatement();
            ResultSet rst = stt.executeQuery("Select * from plataformas");
            
            while (rst.next()) {
                boxPlataforma.addItem(rst.getString(2));
            }
        } catch (Exception ex) {
            System.out.println("Mensaje: " + ex.getMessage());
        }
    }
    
    public void mostrarDatos() {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("ID");
        dtm.addColumn("Plataforma");
        dtm.addColumn("Descripcion");
        
        DataTable.setModel(dtm);
        
        try {
            String [] data = new String[3];
            Statement set = cn.createStatement();
            ResultSet rst = set.executeQuery("SELECT * FROM plataformas");
            while(rst.next()) {
                data[0] = rst.getString(1);
                data[1] = rst.getString(2);
                data[2] = rst.getString(3);
                dtm.addRow(data);
            }
            DataTable.setModel(dtm);
        } catch (Exception ex) {
            System.out.println("Mensaje: \n" + ex.getMessage());
        }
    }

