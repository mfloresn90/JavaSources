    private void BtnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnOKActionPerformed

        if (!plataform.getText().equals("") && !descript.getText().equals("")) {
            try {
                PreparedStatement sent = cn.prepareStatement("INSERT INTO plataformas (nombre, descripcion) VALUES (?, ?)");
                sent.setString(1, plataform.getText());
                sent.setString(2, descript.getText());
                sent.executeUpdate();
                mostrarDatos();
            } catch (Exception ex) {
                System.out.println("Sorry \n" + ex.getMessage());
            }

            plataform.setText("");
            descript.setText("");
            System.out.println("Exito!! :P");
            JOptionPane.showMessageDialog(null, "Exito!! :)");
        } else {
            System.out.println("Sorry my friend :(");
            JOptionPane.showMessageDialog(null, "Sorry my friend!! :'(");
        }
        
        BtnOK.setEnabled(false);
    }//GEN-LAST:event_BtnOKActionPerformed

