    private void plataformKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_plataformKeyReleased
        if (!plataform.getText().equals("")) {
            txtField = true;
        } else {
            txtField = false;
        }
        if (txtField == true && txtArea == true) {
            BtnOK.setEnabled(true);
        } else {
            BtnOK.setEnabled(false);
        }
    }//GEN-LAST:event_plataformKeyReleased

