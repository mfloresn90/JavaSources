    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        String idPoeta = (String) poemaIdPoeta.getSelectedItem();
        
        String[] parts = idPoeta.split("-");
        
        try {
            PreparedStatement sent = cn.prepareStatement("INSERT INTO poema (idPoeta, titulo, tipo, fecha, nacionalidad) VALUES (?, ?, ?, ?, ?)");
            sent.setString(1, parts[0]);
            sent.setString(2, poemTitulo.getText());
            sent.setString(3, (String) poemaTipo.getSelectedItem());
            sent.setString(4, poemFecha.getText());
            sent.setString(5, (String) poemaNacionalidad.getSelectedItem());
            sent.executeUpdate();
            showDataP();
        } catch (Exception ex) {
            System.out.println("Sorry \n" + ex.getMessage());
        }

        clearFields();
        System.out.println("Exito!! :P");
        JOptionPane.showMessageDialog(null, "Exito!! :)");
        
        showDataPa();

    }//GEN-LAST:event_jButton2ActionPerformed

