    private void pSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pSaveActionPerformed

        try {
            PreparedStatement sent = cn.prepareStatement("INSERT INTO poeta (nombre, aPaterno, aMaterno, email, pseudonimo, sexo, nacionalidad) VALUES (?, ?, ?, ?, ?, ?, ?)");
            sent.setString(1, pName.getText());
            sent.setString(2, pLname.getText());
            sent.setString(3, pMlname.getText());
            sent.setString(4, Pemail.getText());
            sent.setString(5, pNickname.getText());
            sent.setString(6, pGen.getSelectedObjects().toString());
            sent.setString(7, pNaci.getSelectedObjects().toString());
            sent.executeUpdate();
            showDataP();
        } catch (Exception ex) {
            System.out.println("Sorry \n" + ex.getMessage());
        }

        clearFields();
        System.out.println("Exito!! :P");
        JOptionPane.showMessageDialog(null, "Exito!! :)");


        pSave.setEnabled(false);
    }//GEN-LAST:event_pSaveActionPerformed

