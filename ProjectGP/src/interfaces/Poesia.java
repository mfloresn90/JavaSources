    public void showDataPa() {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("ID Poema");
        dtm.addColumn("Poeta");
        dtm.addColumn("Titulo");
        dtm.addColumn("Tipo");
        dtm.addColumn("Fecha");
        dtm.addColumn("Nacionalidad");
        
        DataTablePa.setModel(dtm);
        
        try {
            String [] data = new String[6];
            Statement set = cn.createStatement();
            ResultSet rst = set.executeQuery("SELECT * FROM poema");
            
            while(rst.next()) {
                
                Statement set2 = cn.createStatement();
                ResultSet rst2 = set2.executeQuery("SELECT nombre, aPaterno FROM poeta WHERE idPoeta = " + rst.getString(2));
                
                data[0] = rst.getString(1);
                while(rst2.next()) {
                    data[1] = rst2.getString(1) + " " + rst2.getString(2);
                }
                data[2] = rst.getString(3);
                data[3] = rst.getString(4);
                data[4] = rst.getString(5);
                data[5] = rst.getString(6);
                dtm.addRow(data);
            }
            DataTablePa.setModel(dtm);
        } catch (Exception ex) {
            System.out.println("Mensaje: \n" + ex.getMessage());
        }
    }

